    private void btnCalculatePriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculatePriceActionPerformed
        // TODO add your handling code here:
        if(txtQty.getText().equals("")){ //check fields empty
            JOptionPane.showMessageDialog(null, "Please Fill in the Quantity", "Required Fields", JOptionPane.ERROR_MESSAGE);
        }else {
            int id = Integer.parseInt(lblID.getText());
            int qty = Integer.parseInt(txtQty.getText());            
            int mod = qty % 20;
            if (mod != 0){
                JOptionPane.showMessageDialog(null, "Quanity should be in multiple of 20", "Wholesale Quantity", JOptionPane.ERROR_MESSAGE);
            }else{
                try {
                    Registry reg = LocateRegistry.getRegistry("127.0.0.1", 1099);
                    ComputeInterface ci = (ComputeInterface)reg.lookup("wholesale_toy_server");
                    double discPrice = ci.calculateToyCost(id, qty);

                    String strPrice = String.format("%.2f", discPrice);
                    lbltotalPrice.setText(strPrice);
                
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex);
                    //ex.printStackTrace();
                }
            }
        }
        
    }//GEN-LAST:event_btnCalculatePriceActionPerformed

