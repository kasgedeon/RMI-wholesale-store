    private void btnPrintReceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintReceiptActionPerformed
        // TODO add your handling code here:
        
        if(txtQty.getText().equals("")){ //check fields empty
            JOptionPane.showMessageDialog(null, "Please Fill in the fields to update", "Required Fields", JOptionPane.ERROR_MESSAGE);
        }else{
            PrintReceipt printR = new PrintReceipt();
            printR.setVisible(true);
            printR.pack();
            printR.setLocationRelativeTo(null);            
            
            //===Reference number===
            int refs;
            refs = 1235 + (int) (Math.random()*4526);
            int ref = (refs);

            //===Transaction Time===
            Calendar timer = Calendar.getInstance();
            timer.getTime();
            SimpleDateFormat tTime = new SimpleDateFormat("HH:mm:ss");
            tTime.format(timer.getTime());
            SimpleDateFormat Tdate = new SimpleDateFormat("dd-MM-yyyy");
            Tdate.format(timer.getTime());
            
            //====Total Price Before Discount====
            int toyQty = Integer.parseInt(txtQty.getText());
            double unitPrice = Double.parseDouble(lblPrice.getText());
            double totalCost = toyQty * unitPrice;
            String strTotPrice = String.format("%.2f", totalCost);
            
            printR.txtReceipt.append("\n         "
                + "WHOLESALE TOY SERVICE\n" +
                "         ======================\n\n" +
                "Reference:\t\t" + ref + "\n" +
                    "-------------------------"
                    + "---------------------------\n\n" +
                "Product Id:\t\t " + lblID.getText() + "\n" +
                "Product Name:\t " + lblName.getText() + "\n" +
                "Quantity:\t\t " + txtQty.getText() + "\n" +
                    "==============================\n" +               
                "Unit Price:\t\t  " + lblPrice.getText() + "\n" +
                "Total Price:\t\t " + strTotPrice + "\n" +
                "Discounted Price:\t" + lbltotalPrice.getText() + "\n" +
                    "==============================\n" +
                "Date: " + Tdate.format(timer.getTime()) + "         " +
                " Time: " + tTime.format(timer.getTime()) +
                "\n\nServed by Raissa - Thank You");
        }
    }//GEN-LAST:event_btnPrintReceiptActionPerformed

