    private void btnGoDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoDeleteActionPerformed
        // TODO add your handling code here:
        boolean deleted = false;
              
        if(txtID.getText().equals("")){ //check ID fields empty
            JOptionPane.showMessageDialog(null, "Cannot resolve ID of Toy to delete", "Required Fields", JOptionPane.ERROR_MESSAGE);
        }else{
            int toyId = Integer.parseInt(txtID.getText());
            try {
                Registry reg = LocateRegistry.getRegistry("127.0.0.1", 1099);
                ComputeInterface ci = (ComputeInterface)reg.lookup("wholesale_toy_server");
                deleted = ci.deleteToyPrice(toyId);

                if(deleted==true) {
                    JOptionPane.showMessageDialog(null, "Toy Price record was successfully deleted!\n"
                        + "Go back to list of toys available"); //Welcome message
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to delete record! \nPlease try again", "Access Denied", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
                //ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnGoDeleteActionPerformed

    private void btnComputePriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComputePriceActionPerformed
        // TODO add your handling code here:
        if(txtName.getText().equals("") || txtPrice.getText().equals("")){ //check fields empty
            JOptionPane.showMessageDialog(null, "Cannot resolve Missing Name or Price", "Required Fields", JOptionPane.ERROR_MESSAGE);
        }else{
            calculateToyCost ctc = new calculateToyCost();
            ctc.setVisible(true);
            ctc.pack();
            ctc.setLocationRelativeTo(null);
            
            ctc.lblID.setText(txtID.getText());
            ctc.lblName.setText(txtName.getText());
            ctc.lblPrice.setText(txtPrice.getText());
            
            this.dispose();
        }
    }//GEN-LAST:event_btnComputePriceActionPerformed

