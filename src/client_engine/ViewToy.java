    private void btnUpdatePriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePriceActionPerformed
        // TODO add your handling code here:
        boolean updatedToy = false;
        if(txtID.getText().equals("") || txtName.getText().equals("") || txtPrice.getText().equals("") || txtStockQty.getText().equals("")){ //check fields empty
            JOptionPane.showMessageDialog(null, "Please Fill in the fields to update", "Required Fields", JOptionPane.ERROR_MESSAGE);
        }else{
            int id = Integer.parseInt(txtID.getText());
            double price = Double.parseDouble(txtPrice.getText());
            int stockQty = Integer.parseInt(txtStockQty.getText()); 
            try {
                Registry reg = LocateRegistry.getRegistry("127.0.0.1", 1099);
                ComputeInterface ci = (ComputeInterface)reg.lookup("wholesale_toy_server");
                updatedToy = ci.updateToyPrice(id, txtName.getText(), price, stockQty);

                if(updatedToy==true) {
                    JOptionPane.showMessageDialog(null, "Record successfully Updated!\n"
                        + "Go to toy table list"); //Success message
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Update Failed! \nPlease try again", "Access Denied", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
                //ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnUpdatePriceActionPerformed

